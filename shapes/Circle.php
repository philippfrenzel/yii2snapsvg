<?php

namespace net\frenzel\snapsvg\shapes;


/**
 * Class Cirlce will hold the abstract methods for drawing a circle
 */
class Circle
{
	/**
	 * x coordinate of the center
	 * @var number
	 */
	public $x = 0;

	/**
	 * y coordinate of the center
	 * @var number
	 */
	public $y = 0;

	/**
	 * r radius of the circle
	 * @var number
	 */
	public $r = 1;

}